# Upper Case
a="Hello Bhagyashree"
print(a.upper())

# Lower Case
b="Hello Bhagyashree"
print(b.lower())

# Slice String by (Strip()) method
c="  Hello Bhagyashree  "
print(c.strip())

# Replace String
d="Hello Bhagyashree"
print(d.replace('Hello','Hi'))

# Split String
e="Hello Bhagyashree"
print(e.split(","))

# String Formatting
age=24
print(f"My name is Bhagyahsree & i am {age} now")