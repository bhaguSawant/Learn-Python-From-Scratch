#Loop
# In tuple For Loop & While Loop
# 1. By using For Loop

thistuple=('A','B','C','D')
for x in thistuple:
    print(x)


# Loop Through the Index Numbers
thistuple1=("Apple","Banana","Zebra")
for i in range(len(thistuple1)):
    print(thistuple1[i])

# 2. By Using While loop
thistuple2=('BB','AA','ZZ','QQ','CC')
i=0
while i<len(thistuple2):
    print(thistuple2[i])
    i=i+1